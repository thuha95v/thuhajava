    private void tblDKTinChiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDKTinChiMouseClicked
        selectedRow = tblDKTinChi.getSelectedRow();
        if (selectedRow >= 0) {
            GVDangKy gvdk = listgvdk.get(selectedRow);
            GiangVien gv = qlgvc.getGVByMaGV(gvdk.getMaGV());
            LopHoc lh;
            try {
                lh = qllhc.getLH(gvdk.getMaLH());
                MonHoc mh = qlmhc.getMH(lh.getMaMH());
                PhongHoc ph = qlphc.getPH(lh.getMaPH());
                txtMaLH.setText(gvdk.getMaLH());
                txtTenLH.setText(lh.getTenLH());
                txtTiet.setText(lh.getTiet());
                txtNgayBatDau.setText(lh.getNgayBatDau());
                cbxMaMH.setSelectedItem(lh.getMaMH());
                txtTenGV.setText(gv.getTenGV());
                txtSoTC.setText("" + mh.getSoTinChi());
                lbThu.setText(lh.getThu());
            } catch (ParseException ex) {
                Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblDKTinChiMouseClicked

    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        int choice = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc muốn đăng ký môn này không?",
                "Hỏi", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        SinhVien sv = qlsvc.getSVById(this.id);
        String maSV = sv.getMaSV();
        selectedRow = tblDKTinChi.getSelectedRow();
        if (selectedRow >= 0) {
            GVDangKy gvdk = listgvdk.get(selectedRow);
            String maLH = gvdk.getMaLH();
            qlsvdkc.dangKyLopHocSV(maSV, maLH);
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String maMH = cbxMaMH.getSelectedItem().toString();
        List<LopHoc> listLHSub = new ArrayList<>();
        try {
            listLHSub = qllhc.timKiemByMaMH(maMH);
        } catch (ParseException ex) {
            Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<GVDangKy> listGVDK = new ArrayList<>();

        for (LopHoc lh : listLHSub) {
            GVDangKy gvdk;
            try {
                gvdk = qlgvdkc.timKiemLopHocGVDaDangKy(lh.getMaLH());
                listGVDK.add(gvdk);
            } catch (ParseException ex) {
                Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }

        tblModel.setRowCount(0);
        int index = 1;
        for (GVDangKy gvdk : listGVDK) {
            GiangVien gv = qlgvc.getGVByMaGV(gvdk.getMaGV());
            LopHoc lh = new LopHoc();
            try {
                lh = qllhc.getLH(gvdk.getMaLH());
            } catch (ParseException ex) {
                Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            MonHoc mh = new MonHoc();
            try {
                mh = qlmhc.getMH(lh.getMaMH());
            } catch (ParseException ex) {
                Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            PhongHoc ph = qlphc.getPH(lh.getMaPH());
            tblModel.addRow(new Object[]{index++, lh.getMaLH(), lh.getTenLH(), mh.getTenMH(), lh.getThu(),
                lh.getTiet(), lh.getNgayBatDau(), lh.getSiSo(), gv.getTenGV(), ph.getTenPH()});
        }
        tblModel.fireTableDataChanged();
    }//GEN-LAST:event_btnTimKiemActionPerformed

