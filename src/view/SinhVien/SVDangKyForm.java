    private void initTable() {
        String[] columns = new String[]{"STT", "Mã Lớp", "Tên Lớp", "Tên Môn", "Thứ",
            "Tiết", "Ngày Bắt Đầu", "Sĩ Số", "Giảng Viên", "Phòng"};
        tblModel.setColumnIdentifiers(columns);
        tblDKTinChi.setModel(tblModel);
    }

    private void fillTable() {
        tblModel.setRowCount(0);
        listgvdk = qlgvdkc.getListLopHocTatCaGVDangKy();
        int index = 1;
        for (GVDangKy gvdk : listgvdk) {
            GiangVien gv = qlgvc.getGVByMaGV(gvdk.getMaGV());
            try {
                LopHoc lh = qllhc.getLH(gvdk.getMaLH());
                MonHoc mh = qlmhc.getMH(lh.getMaMH());
                PhongHoc ph = qlphc.getPH(lh.getMaPH());
                tblModel.addRow(new Object[]{index++, lh.getMaLH(), lh.getTenLH(), mh.getTenMH(), lh.getThu(),
                    lh.getTiet(), lh.getNgayBatDau(), lh.getSiSo(), gv.getTenGV(), ph.getTenPH()});
                
            } catch (ParseException ex) {
                Logger.getLogger(SVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        tblModel.fireTableDataChanged();
    }

