    private boolean isHadEmptyField() {
        StringBuffer sb = new StringBuffer();
        if (txtID.getText().isBlank()) {
            sb.append("Tên đăng nhập không đc để trống\n");
            txtID.setBackground(Color.CYAN);
        } else {
            txtID.setBackground(null);
        }
        if (txtID.getText().trim().length() > 10) {
            sb.append("Tên đăng nhập không hợp lệ\n");
            txtID.setBackground(Color.CYAN);
        } else {
            txtID.setBackground(null);
        }
        if (psMatKhau.getText().isBlank()) {
            sb.append("Mật khẩu không đc để trống\n");
            psMatKhau.setBackground(Color.CYAN);
        } else {
            psMatKhau.setBackground(null);
        }
        if (psMatKhau.getText().length() > 20) {
            sb.append("Mật khẩu vượt quá độ dài cho phép\n");
            psMatKhau.setBackground(Color.CYAN);
        } else {
            psMatKhau.setBackground(null);
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb, "Error", JOptionPane.ERROR_MESSAGE);
            return true;
        }
        return false;
    }

