    private void initTable() {
        String[] columns = new String[]{"STT", "Tên đăng nhập", "Mật khẩu", "Loại tài khoản"};
        tblModel.setColumnIdentifiers(columns);
        tblTaiKhoan.setModel(tblModel);
    }

    private void fillTable() {
        tblModel.setRowCount(0);
        listTK = tkc.getListTK();
        int index = 1;
        for (TaiKhoan taiKhoan : listTK) {
            tblModel.addRow(new Object[]{index++, taiKhoan.getId(), taiKhoan.getMatKhau(), taiKhoan.getLoaiTK()});
        }
        tblModel.fireTableDataChanged();
    }

