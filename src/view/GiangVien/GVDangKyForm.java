    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        int choice = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc muốn đăng ký môn này không?",
                "Hỏi", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        GiangVien gv = new QLGiangVienController().getGVById(this.id);
        String maGV = gv.getMaGV();
        selectedRow = tblGVDangKy.getSelectedRow();
        if (selectedRow >= 0) {
            LopHoc lh = listLH.get(selectedRow);
            String maLH = lh.getMaLH();
            qlgvdkc.dangKyLopHocGV(maGV, maLH);
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String maMH = cbxMaMH.getSelectedItem().toString();
        try {
            List<LopHoc> listLH = new QLLopHocController().timKiemByMaMH(maMH);
            List<GVDangKy> listGVDK = new ArrayList<>();

            tblModel.setRowCount(0);
            int index = 1;
            for (LopHoc lh : listLH) {
                MonHoc mh = new QLMonHocController().getMH(lh.getMaMH());
                PhongHoc ph = new QLPhongHocController().getPH(lh.getMaPH());
                tblModel.addRow(new Object[]{index++, lh.getMaLH(), lh.getTenLH(), mh.getTenMH(),
                    lh.getThu(), lh.getTiet(), lh.getNgayBatDau(), lh.getSiSo(), ph.getTenPH()});
            }
            tblModel.fireTableDataChanged();
        } catch (ParseException ex) {
            Logger.getLogger(GVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tblGVDangKyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGVDangKyMouseClicked
        selectedRow = tblGVDangKy.getSelectedRow();
        if (selectedRow >= 0) {
            LopHoc lh = listLH.get(selectedRow);
            try {
                MonHoc mh = new QLMonHocController().getMH(lh.getMaMH());
                PhongHoc ph = new QLPhongHocController().getPH(lh.getMaPH());
                txtMaLH.setText(lh.getMaLH());
                txtTenLH.setText(lh.getTenLH());
                txtTiet.setText(lh.getTiet());
                txtNgayBatDau.setText(lh.getNgayBatDau());
                cbxMaMH.setSelectedItem(lh.getMaMH());
                txtSiSo.setText(lh.getSiSo() + "");
                txtSoTC.setText("" + mh.getSoTinChi());
                lbThu.setText(lh.getThu());
            } catch (ParseException ex) {
                Logger.getLogger(GVDangKyForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblGVDangKyMouseClicked

