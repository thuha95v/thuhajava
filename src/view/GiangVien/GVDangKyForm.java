    private void initTable() {
        String[] columns = new String[]{"STT", "Mã Lớp", "Tên Lớp", "Môn Học", "Thứ",
            "Tiết", "Ngày Bắt Đầu", "Sĩ Số", "Phòng"};
        tblModel.setColumnIdentifiers(columns);
        tblGVDangKy.setModel(tblModel);
    }

    private void fillTable() {
        try {
            tblModel.setRowCount(0);
            listLH = new QLLopHocController().getListLH();
            int index = 1;
            for (LopHoc lh : listLH) {
                MonHoc mh = new QLMonHocController().getMH(lh.getMaMH());
                PhongHoc ph = new QLPhongHocController().getPH(lh.getMaPH());
                tblModel.addRow(new Object[]{index++, lh.getMaLH(), lh.getTenLH(), mh.getTenMH(),
                    lh.getThu(), lh.getTiet(), lh.getNgayBatDau(), lh.getSiSo(), ph.getTenPH()});
            }
            tblModel.fireTableDataChanged();
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

    }

    private void initCombobox() throws ParseException {
        List<MonHoc> listMH = new QLMonHocController().getListMH();
        for (MonHoc mh : listMH) {
            cbxMaMH.addItem(mh.getMaMH());
        }
    }

