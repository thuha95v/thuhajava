    private void btnCapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhapActionPerformed
        // cập nhập thông tin giảng viên
        int choice = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc muốn cập nhập thông tin hay không?",
                "Hỏi", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        if (!isHadEmptyField()) {
            String maGV = txtMaGV.getText().trim();
            String tenGV = txtTenGV.getText().trim();
            String khoa = txtKhoa.getText().trim();
            String soDT = txtSoDT.getText().trim();
            String email = txtEmail.getText().trim();
            gv = new GiangVien(maGV, tenGV, khoa, soDT, email, this.id);
            qlgvc.suaGV(gv);
        }
    }//GEN-LAST:event_btnCapNhapActionPerformed

