    private void viewInfor() {
        gv = qlgvc.getGVById(id);
        txtMaGV.setText(gv.getMaGV());
        txtTenGV.setText(gv.getTenGV());
        txtKhoa.setText(gv.getKhoa());
        txtSoDT.setText(gv.getSoDT());
        txtEmail.setText(gv.getEmail());
    }

    private void resetData() {
        String empty = "";
        txtMaGV.setText(empty);
        txtTenGV.setText(empty);
        txtKhoa.setText(empty);
        txtSoDT.setText(empty);
        txtEmail.setText(empty);
        txtMaGV.setBackground(null);
        txtTenGV.setBackground(null);
        txtKhoa.setBackground(null);
        txtSoDT.setBackground(null);
        txtEmail.setBackground(null);
    }

    private boolean isHadEmptyField() {
        StringBuffer sb = new StringBuffer();
        if (txtMaGV.getText().isBlank()) {
            sb.append("Mã giảng viên không đc để trống\n");
            txtMaGV.setBackground(Color.CYAN);
        } else {
            txtMaGV.setBackground(null);
        }
        if (txtTenGV.getText().isBlank()) {
            sb.append("Tên giảng viên không đc để trống\n");
            txtTenGV.setBackground(Color.CYAN);
        } else {
            txtTenGV.setBackground(null);
        }
        if (txtKhoa.getText().isBlank()) {
            sb.append("Khoa không đc để trống\n");
            txtKhoa.setBackground(Color.CYAN);
        } else {
            txtKhoa.setBackground(null);
        }
        if (txtSoDT.getText().isBlank() || txtSoDT.getText().trim().length() != 10) {
            sb.append("Số điện thoại không đc để trống và phải điền đủ 10 số\n");
            txtSoDT.setBackground(Color.CYAN);
        } else {
            txtSoDT.setBackground(null);
        }
        if (txtEmail.getText().isBlank()) {
            sb.append("Email không đc để trống\n");
            txtEmail.setBackground(Color.CYAN);
        } else {
            txtEmail.setBackground(null);
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb, "Error", JOptionPane.ERROR_MESSAGE);
            return true;
        }
        return false;
    }

