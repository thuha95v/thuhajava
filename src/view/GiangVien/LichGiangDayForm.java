    private void tblLichGiangDayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLichGiangDayMouseClicked
        selectedRow = tblLichGiangDay.getSelectedRow();
        if (selectedRow >= 0) {
            LopHoc lh = listLHGVDK.get(selectedRow);
            MonHoc mh;
            try {
                mh = new QLMonHocController().getMH(lh.getMaMH());
                PhongHoc ph = new QLPhongHocController().getPH(lh.getMaPH());
                lbMaLH.setText(lh.getMaLH());
                lbTenLH.setText(lh.getTenLH());
                lbTenMH.setText(mh.getTenMH());
                lbSoTC.setText(mh.getSoTinChi() + "");
                lbTiet.setText(lh.getTiet());
                lbThu.setText(lh.getThu());
                lbNgayBatDau.setText(lh.getNgayBatDau() + "");
                lbTenPH.setText(ph.getTenPH());
            } catch (ParseException ex) {
                Logger.getLogger(LichGiangDayForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblLichGiangDayMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc muốn xóa môn học này không?",
                "Hỏi", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        selectedRow = tblLichGiangDay.getSelectedRow();
        if (selectedRow >= 0) {
            LopHoc lh = listLHGVDK.get(selectedRow);
            GiangVien gv = new QLGiangVienController().getGVById(this.id);
            new QLGVDangKyController().xoaLopHocGV(gv.getMaGV(), lh.getMaLH());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn 1 dòng để xóa");
        }
        try {
            fillTable();
        } catch (ParseException ex) {
            Logger.getLogger(LichGiangDayForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        resetData();
    }//GEN-LAST:event_jButton1ActionPerformed

