    private void initTable() {
        String[] columns = new String[]{"Thứ", "Tiết", "Môn Học", "Phòng"};
        tblModel.setColumnIdentifiers(columns);
        tblLichGiangDay.setModel(tblModel);
    }

    private void fillTable() throws ParseException {
        GiangVien gv = new QLGiangVienController().getGVById(this.id);
        tblModel.setRowCount(0);
        listLHGVDK = qlgvdkc.getListLopHoc1GVDangKy(gv.getMaGV());
        Collections.sort(listLHGVDK);
        for (LopHoc lh : listLHGVDK) {
            MonHoc mh = new QLMonHocController().getMH(lh.getMaMH());
            PhongHoc ph = new QLPhongHocController().getPH(lh.getMaPH());
            tblModel.addRow(new Object[]{lh.getThu(), lh.getTiet(), mh.getTenMH(), ph.getTenPH()});
        }
        tblModel.fireTableDataChanged();
    }

